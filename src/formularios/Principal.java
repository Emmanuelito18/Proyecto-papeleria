    public void activarBotones(int a,int b, int c){
        this.botonArts=a;
        this.botonMarcas=b;
        this.botonUsuarios=c;
        if(botonArts==1){
            btnArts.setEnabled(true);
        }
        else{
            btnArts.setEnabled(false);
        }
            if(botonUsuarios==1){
                btnUsuarios.setEnabled(true);
            }
            else{
                btnUsuarios.setEnabled(false);
            }
            if(botonMarcas==1){
                btnMarcas.setEnabled(true);
            }
            else{
                btnMarcas.setEnabled(false);
            }
    }
