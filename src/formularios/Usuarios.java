    private void ExplorarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ExplorarActionPerformed
        // TODO add your handling code here:
        JFileChooser archivoImagen=new JFileChooser();
        FileNameExtensionFilter filtro=new FileNameExtensionFilter("Imágenes","jpg","png","gif","bmp");
        archivoImagen.setFileFilter(filtro);
        archivoImagen.showOpenDialog(this);
        fichero=archivoImagen.getSelectedFile();
        if(fichero!=null){
            txtArchivo.setText(fichero.getName());
            txtUbica.setText(fichero.getAbsolutePath());
            bytesImagen=new byte[512000];
            try{
                archivo=new FileInputStream(fichero);
                archivo.read(bytesImagen);
                if(archivo.read()<=512000){
                    lblFoto.setIcon(new ImageIcon(bytesImagen));
                    lblFoto.setText("");
                    foto=null;
                }
                else{
                    JOptionPane.showMessageDialog(rootPane,"La imagen no debe exceder 512Kb");
                }
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(archivoImagen,"Error al obtener");
            }
        }
    }//GEN-LAST:event_ExplorarActionPerformed

