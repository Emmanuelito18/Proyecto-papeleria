    private void guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarActionPerformed
        // TODO add your handling code here:
        PreparedStatement comando=null;
        try{
            int a,b,c;
            if(txtNombre.getText().isEmpty() || txtPass.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Caprura los datos para identificar al usuario");
            }else{
                if(txtPass.getText().matches(txtPass2.getText())){
                    if(actualiza==false){
                        String insertar="INSERT INTO usuarios(nombre,password,articulos,marcas,users,fotografia) VALUES(?,?,?,?,?,?)";
                         comando=(PreparedStatement)conexion.conectar.prepareStatement(insertar);
                    }
                    else{
                        String cambiar="UPDATE usuarios SET nombre=?,password=?,articulos=?,marcas=?,users=?,fotografia=? WHERE id_usuario='"+idUsuario+"'";
                        comando=(PreparedStatement)conexion.conectar.prepareStatement(cambiar);
                        actualiza=false;
                    }
                comando.setString(1, txtNombre.getText());
                comando.setString(2, txtPass.getText());
                a=chkArt.isSelected()?1:0;
                comando.setString(3, String.valueOf(a));
                b=chkMarca.isSelected()?1:0;
                comando.setString(4, String.valueOf(b));
                c=chkUser.isSelected()?1:0;
                comando.setString(5, String.valueOf(c));
                if(foto==null) foto=new FileInputStream(new File(fichero.getAbsolutePath()));
                comando.setBinaryStream(6,foto);
                comando.executeUpdate();
               JOptionPane.showMessageDialog(null, "Usuario registrado exitosamente");
               limpiarComponentes();
               deshabilitarComponentes();
                }else{
                JOptionPane.showMessageDialog(null,"Los password's no coinciden");
                txtPass.setText("");
                txtPass2.setText("");
                txtPass.grabFocus();
                }
            }
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, "Error al Registrar al usuario  verifique.....");
        } catch (FileNotFoundException ex) {
            JOptionPane.showMessageDialog(rootPane,"Error al obtener el archivo de la imagen");
        }
    }//GEN-LAST:event_guardarActionPerformed

    private void buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarActionPerformed
        // TODO add your handling code here:
        try{
            limpiarComponentes();
            deshabilitarComponentes();
            String nom=JOptionPane.showInputDialog("Usuario a buscar");
            String consulta="SELECT * FROM usuarios WHERE nombre=?";
            PreparedStatement comando=(PreparedStatement)conexion.conectar.prepareStatement(consulta);
            comando.setString(1,nom);
            ResultSet registro=comando.executeQuery();
            if(registro.next()){
                idUsuario=registro.getString(1);
                txtNombre.setText(registro.getString(2));
                txtPass.setText(registro.getString(3));
                txtPass2.setText(registro.getString(3));
                if(registro.getInt(4)==1)chkArt.setSelected(true);
                if(registro.getInt(5)==1)chkMarca.setSelected(true);
                if(registro.getInt(6)==1)chkUser.setSelected(true);
                if(registro.getBytes(7)!=null){
                    bytesImagen=registro.getBytes(7);
                    foto=registro.getBinaryStream(7);
                    lblFoto.setIcon(new ImageIcon(bytesImagen));
                }
                else{
                    lblFoto.setIcon(null);
                    lblFoto.setText("Sin imagen");
                    foto=null;
                }
            }    
                else{
                    JOptionPane.showMessageDialog(rootPane,"Usuario inexistente...");
                }
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(rootPane,"Error al efectuar la consulta");
        }
                
    }//GEN-LAST:event_buscarActionPerformed

